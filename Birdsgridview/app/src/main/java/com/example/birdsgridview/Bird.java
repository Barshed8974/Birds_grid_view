package com.example.birdsgridview;

public class Bird {
    private int mImage;
    private String mName;

    public Bird(int mImage, String mName) {
        this.mImage = mImage;
        this.mName = mName;
    }

    public int getmImage() {
        return mImage;
    }

    public String getmName() {
        return mName;
    }
}
